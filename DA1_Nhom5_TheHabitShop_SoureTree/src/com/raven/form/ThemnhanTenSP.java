    boolean checkSP() {
        String tenSP = txtTenSPTNTT.getText().trim(); // Lấy tên sản phẩm từ JTextField và loại bỏ khoảng trắng ở đầu và cuối

        // Kiểm tra xem tên sản phẩm có rỗng không
        if (tenSP.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng nhập tên sản phẩm!");
            return false; // Trả về null nếu có lỗi
        }
        return true;
    }

    com.raven.classmodel.SanPham readForm() {
        String tenSP = txtTenSPTNTT.getText().trim(); // Lấy tên sản phẩm từ JTextField và loại bỏ khoảng trắng ở đầu và cuối

        GenMaTuDong genTD = new GenMaTuDong() {
            @Override
            public String maTuDong() {
                int gen = new Random().nextInt(1000000);
                return "SP" + gen;
            }
        };

        String maSp = genTD.maTuDong();
        return new com.raven.classmodel.SanPham(maSp, tenSP);
    }

